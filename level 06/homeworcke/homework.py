 # Case-sensitive (რეგისტრის მიმართ მგრძნობიარე) 
 # ნიშნავს, რომ სისტემაში დიდ და პატარა ასოებს ერთმანეთისგან განასხვავებენ. მაგალითად, პროგრამირებაში "Hello" და "hello" შეიძლება იყოს სხვადასხვა მნიშვნელობის მქონე ცვლადები ან ტექსტები.

 # Case-insensitive (რეგისტრის მიმართ არამგრძნობიარე) 
 # ნიშნავს, რომ სისტემაში დიდ და პატარა ასოებს ერთნაირად აღიქვამენ. მაგალითად, ზოგიერთ საძიებო სისტემაში "apple" და "APPLE" ერთიდაიგივე მნიშვნელობის ტექსტად განიხილება.